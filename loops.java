class loops{

    public static void main(String a[]){
        int i=1;
        while(i<10){
            System.out.println(i);
            i++;
        }


        do {
            System.out.println(i);
            i++;
        }while(i<10);


        for (int j=0;j<5;j++){
            System.out.println(i);
        }
    }
}


